s0 = u'AAAAEEEIIOOOOUUYaaaaeeeiioooouuyAaDdIiUuOoUuAaAaAaAaAaAaAaAaAaAaAaAaEeEeEeEeEeEeEeEeIiIiOoOoOoOoOoOoOoOoOoOoOoOoUuUuUuUuUuUuUuYyYyYyYy'
def remove_accents(input_str):
    """Remove accents from Vietnamese words

    Args:
        input_str (str): [description]

    Returns:
        str: tone removed version of input_str
    """

    s = ''
    for c in input_str:
        if c in s1:
            s += s0[s1.index(c)]
        else:
            s += c
    return s

def clean_column_name(df):
    """Convert dataframe column name to underscore casing

    Args:
        df (pandas.DataFrame): Input DataFrame

    Returns:
        pandas.DataFrame: DataFrame after rename
    """
    df.columns = [remove_accents(i.strip().lower()).replace(" ",'_') for i in df.columns]
    return df

